#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		count;
	t_list	*current;

	if (!lst)
		return (0);
	count = 1;
	current = lst;
	while (current->next)
	{
		count++;
		current = current->next;
	}
	return (count);
}
